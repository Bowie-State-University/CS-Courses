
// This program illustrates how mixed expressions are evaluated.

#include <iostream>

using namespace std;

int main() 
{
    cout << "3 / 2 + 5.5 = " << 3 / 2 + 5.5 << endl;
    cout << "15.6 / 2 + 5 = " << 15.6 / 2 + 5 << endl;
    cout << "4 + 5 / 2.0 = " << 4 + 5 / 2.0 << endl;
    cout << "4 * 3 + 7 / 5 - 25.5 = " 
         << 4 * 3 + 7 / 5 - 25.5 
         << endl;
    
    return 0;
}


